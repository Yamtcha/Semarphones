public class Trips {

    private int branch;
    private int duration;

    public Trips(int branch, int duration) {
        this.branch = branch;
        this.duration = duration;
    }

    public int getBranch() {
        return branch;
    }

    public int getDuration() {
        return duration;
    }
}
